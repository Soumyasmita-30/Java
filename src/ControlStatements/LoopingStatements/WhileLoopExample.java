package ControlStatements.LoopingStatements;

public class WhileLoopExample {
    public static void main(String[] args) {
        int a = 1 ;
        while(a<=10){
            System.out.println(a);
            a++ ;
        }

        int b = 20 ;
        while(b>= 10){
            System.out.println(b);
            b-- ;
        }

    }
}

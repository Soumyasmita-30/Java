package ControlStatements.LoopingStatements;

public class ForLoopExample {
    public static void main(String[] args) {
        int j ;
        for(int i = 0 ;i<= 100 ;i++){
            System.out.println(i);
        }
        for(j = 0 ; j< 10 ; j++){
            System.out.println(j);}

        int k ;
        for(k= 1 ; k<10 ; ++k){
            System.out.println(k);
        }

    }

    }

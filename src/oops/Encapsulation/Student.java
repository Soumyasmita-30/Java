package oops.Encapsulation;

public class Student {
    private String name;
    private int RollNo ;
    private int age ;

    public int getAge() {
        return age;
    }
    public void setAge(int age) {
        this.age = age;
    }
    public int getRollNo() {
        return RollNo;
    }
    public void setRollNo(int rollNo) {
        RollNo = rollNo;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
}
